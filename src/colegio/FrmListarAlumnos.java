    /**
     * Obtiene la lista de Alumnos y los agrega en la
     * tabla del formulario
     */
    public void listarAlumnos(){
        int cantidadAlumnos = GestionColegio.listaAlumnos.size();
        DefaultTableModel modelo = (DefaultTableModel) tblAlumnos.getModel();        
        String[] datos = new String[8];
        for(int i=0;i<cantidadAlumnos;i++){
            datos[0]=GestionColegio.listaAlumnos.get(i).getIdentificacion();
            datos[1]=GestionColegio.listaAlumnos.get(i).getNombre();
            datos[2]=GestionColegio.listaAlumnos.get(i).getApellidos();
            datos[3]=GestionColegio.listaAlumnos.get(i).getCorreo();
            datos[4]=GestionColegio.listaAlumnos.get(i).getGenero();
            datos[5]=GestionColegio.listaAlumnos.get(i).getDireccion();
            datos[6]=GestionColegio.listaAlumnos.get(i).getFechanacimiento();
            datos[7]=GestionColegio.listaAlumnos.get(i).getTelefono();
            
            modelo.addRow(datos);            
        }      
    }

